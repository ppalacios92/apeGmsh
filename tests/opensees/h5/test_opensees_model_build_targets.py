def test_build_h5_byte_equivalent(tmp_path: Path) -> None:
    """``build('h5', out=)`` produces the same file as ``to_h5(out)``
    and matches the source (modulo ``/meta/created_iso``)."""
    src, _ = build_simple_frame_h5(tmp_path)
    om = OpenSeesModel.from_h5(src)
    out_a = tmp_path / "via_build.h5"
    out_b = tmp_path / "via_to_h5.h5"
    om.build("h5", out=out_a)
    om.to_h5(out_b)

    # via_build and via_to_h5 must agree on every group / dataset.
    with h5py.File(out_a, "r") as a, h5py.File(out_b, "r") as b:
        _assert_h5_equal(a, b)


