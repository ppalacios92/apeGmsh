"""ADR 0019 — :meth:`OpenSeesModel.build` target parity / smoke tests.

* ``build('tcl')`` matches the deck :meth:`apeSees.tcl` produces for
  the same model (per-line text equality modulo line endings).
* ``build('py')`` matches the deck :meth:`apeSees.py` produces.
* ``build('h5', out=path)`` is byte-equivalent to
  :meth:`OpenSeesModel.to_h5` and to the source archive
  (modulo ``/meta/created_iso``).
* ``build('live')`` smoke — when openseespy is importable, drives
  an in-process emitter without raising.
* ``build('aster')`` raises ``ValueError``.
* The class docstring loudly documents the tag-divergence rule
  from INV-5.
"""
from __future__ import annotations

import importlib.util
from pathlib import Path

import h5py
import numpy as np
import pytest

from apeGmsh.opensees import OpenSeesModel, apeSees
from apeGmsh.opensees.section.fiber import FiberPoint

from tests.opensees.h5._opensees_model_fixtures import (
    build_simple_frame_fem,
    build_simple_frame_h5,
)


