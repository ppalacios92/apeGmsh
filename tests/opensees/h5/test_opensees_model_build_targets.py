def _tcl_token_counts(text: str) -> dict[str, int]:
    """Count occurrences of each leading Tcl command token.

    For lines like ``"uniaxialMaterial Steel02 1 ..."``, the leading
    token is ``"uniaxialMaterial"``.  Comment lines are skipped.
    """
    counts: dict[str, int] = {}
    for line in text.splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("#"):
            continue
        first = stripped.split(None, 1)[0]
        counts[first] = counts.get(first, 0) + 1
    return counts


def _py_call_counts(text: str) -> dict[str, int]:
    """Count occurrences of each ``ops.<call>(`` shape in a python deck."""
    counts: dict[str, int] = {}
    for line in text.splitlines():
        stripped = line.strip()
        if not stripped or stripped.startswith("#"):
            continue
        # Expect e.g. ``ops.uniaxialMaterial("Steel02", 1, ...)``.
        if stripped.startswith("ops."):
            call = stripped[4:].split("(", 1)[0]
            counts[call] = counts.get(call, 0) + 1
    return counts


def _assert_h5_equal(a: h5py.File, b: h5py.File) -> None:
    """Mirror of the helper in ``test_opensees_model.py`` — kept inline
    so this test file is self-contained."""
    _assert_attrs_equal(a, b, path="/")
    _assert_group_equal(a, b, path="/")


def _assert_group_equal(a: h5py.Group, b: h5py.Group, *, path: str) -> None:
    assert sorted(a.keys()) == sorted(b.keys()), (
        f"{path}: child keys differ"
    )
    for key in sorted(a.keys()):
        a_sub = a[key]
        b_sub = b[key]
        sub_path = f"{path}{key}/"
        _assert_attrs_equal(a_sub, b_sub, path=sub_path)
        if hasattr(a_sub, "shape"):
            assert a_sub.shape == b_sub.shape, sub_path
            assert a_sub.dtype.kind == b_sub.dtype.kind, sub_path
            if a_sub.dtype.kind in ("f", "i", "u"):
                np.testing.assert_array_equal(a_sub[:], b_sub[:])
        else:
            _assert_group_equal(a_sub, b_sub, path=sub_path)


def _assert_attrs_equal(
    a: h5py.HLObject, b: h5py.HLObject, *, path: str,
) -> None:
    a_keys = {k for k in a.attrs.keys() if k != "created_iso"}
    b_keys = {k for k in b.attrs.keys() if k != "created_iso"}
    assert a_keys == b_keys, f"{path}: attr keys differ"
    for key in a_keys:
        va = a.attrs[key]
        vb = b.attrs[key]
        if isinstance(va, bytes):
            va = va.decode("utf-8")
        if isinstance(vb, bytes):
            vb = vb.decode("utf-8")
        if isinstance(va, np.ndarray) or isinstance(vb, np.ndarray):
            np.testing.assert_array_equal(np.asarray(va), np.asarray(vb))
        else:
            assert va == vb, f"{path}: attr {key!r} differs"
