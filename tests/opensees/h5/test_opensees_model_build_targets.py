# Helpers
# ---------------------------------------------------------------------------

def _author_simple_frame_apesees() -> apeSees:
    """Return a freshly authored :class:`apeSees` instance over the
    simple-frame FEM.

    Matches the construction in
    :func:`tests.opensees.h5._opensees_model_fixtures.build_simple_frame_h5`
    so the test bed can compare ``apeSees.tcl`` against
    ``OpenSeesModel.build('tcl')`` line by line.
    """
    fem = build_simple_frame_fem()
    ops = apeSees(fem)
    ops.model(ndm=3, ndf=6)
    steel = ops.uniaxialMaterial.Steel02(fy=420e6, E=200e9, b=0.01)
    sec = ops.section.Fiber(
        GJ=1.0e9,
        fibers=(FiberPoint(material=steel, y=0.0, z=0.0, area=0.01),),
    )
    transf = ops.geomTransf.Linear(vecxz=(1.0, 0.0, 0.0))
    integ = ops.beamIntegration.Lobatto(section=sec, n_ip=5)
    ops.element.forceBeamColumn(
        pg="Cols", transf=transf, integration=integ,
    )
    return ops


# ---------------------------------------------------------------------------
# build('tcl') / build('py') parity
# ---------------------------------------------------------------------------

def test_build_tcl_matches_apesees_tcl(tmp_path: Path) -> None:
    """The deck generated by :meth:`OpenSeesModel.build('tcl')` is a
    superset (or equal set) of the lines :meth:`apeSees.tcl` produces.

    Direct byte equality is not required by ADR 0019 INV-5 — tag
    identity may diverge — but the *structure* (number of node /
    materialUniaxial / section / geomTransf / element / fix lines)
    must match.
    """
    src, _ = build_simple_frame_h5(tmp_path)
    om = OpenSeesModel.from_h5(src)
    om_tcl_text = om.build("tcl")
    assert isinstance(om_tcl_text, str)

    ops = _author_simple_frame_apesees()
    bridge_path = tmp_path / "bridge.tcl"
    ops.tcl(str(bridge_path))
    bridge_tcl_text = bridge_path.read_text(encoding="utf-8")

    om_tokens = _tcl_token_counts(om_tcl_text)
    bridge_tokens = _tcl_token_counts(bridge_tcl_text)
    # Categories the round-trip must preserve count-for-count.
    for cat in (
        "node", "uniaxialMaterial", "section", "geomTransf",
        "beamIntegration", "element", "fix",
    ):
        assert om_tokens.get(cat, 0) == bridge_tokens.get(cat, 0), (
            f"build('tcl') {cat!r} count differs from apeSees.tcl: "
            f"{om_tokens.get(cat)} vs {bridge_tokens.get(cat)}"
        )


def test_build_py_matches_apesees_py(tmp_path: Path) -> None:
    """Same parity contract as :meth:`test_build_tcl_matches_apesees_tcl`
    but on the openseespy deck."""
    src, _ = build_simple_frame_h5(tmp_path)
    om = OpenSeesModel.from_h5(src)
    om_py_text = om.build("py")
    assert isinstance(om_py_text, str)

