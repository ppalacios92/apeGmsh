def test_build_live_documents_tag_divergence() -> None:
    """The class docstring loudly documents the tag-identity caveat
    from ADR 0019 INV-5.

    This isn't a runtime guard — INV-5 says tag identity *may*
    diverge; the documented caveat is the deliverable.
    """
    doc = (OpenSeesModel.__doc__ or "")
    build_doc = (OpenSeesModel.build.__doc__ or "")
    haystack = (doc + "\n" + build_doc).lower()
    assert "tag identity" in haystack
    assert "diverge" in haystack
    assert "inv-5" in haystack


